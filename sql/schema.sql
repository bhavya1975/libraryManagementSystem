  EXECUTE IMMEDIATE 'DROP TABLE Fine CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE Issue_Record CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
